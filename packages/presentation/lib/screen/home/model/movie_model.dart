class MovieModel {
  final String titles;
  final String images;
  final String movieId;
  final String time;
  final double rating;
  final String genres;
  final String certifications;

  MovieModel({
    required this.titles,
    required this.images,
    required this.movieId,
    required this.time,
    required this.rating,
    required this.genres,
    required this.certifications,
  });
}
